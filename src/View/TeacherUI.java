   private void changeOption(int option) {
      for (int i = 0; i < listOption.size(); i++) {
         JPanel panel = listOption.get(i);
         if (i == option)
            panel.setBackground(new Color(67, 149, 241));
         else
            panel.setBackground(new java.awt.Color(98, 43, 229));
         listOption.set(i, panel);
      }
      CardLayout cardLayout = (CardLayout) mainPanel.getLayout();
      switch (option) {
         case 0:
            cardLayout.show(mainPanel, "Student Card");
            break;
         case 1:
            cardLayout.show(mainPanel, "Schedule Card");
            break;
         case 2:
            cardLayout.show(mainPanel, "Class Card");
            break;
         case 3:
            cardLayout.show(mainPanel, "Score Card");
            break;
         case 4:
            cardLayout.show(mainPanel, "Complain Card");
            break;
         case 5:
            cardLayout.show(mainPanel, "Account Card");
            break;

      }
   }

